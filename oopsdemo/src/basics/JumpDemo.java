public class JumpDemo {

	public static void main(String[] args) {
        System.out.println("********** Break Demo ***********");
        
        for(int i=1;i<=10;i++)
        {
        	if(i==6)
        	{
        		break;
        	}
        	System.out.println(i+" Raj");
        }
	}

}
